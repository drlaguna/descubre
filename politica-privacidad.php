<h1>Política de privacidad</h1>

<p>
1. ¿Para qué sirve esta Política de privacidad?
</p>
<p>
¡Bienvenido/a al Proyecto Descubre! La Facultad de Informática de la Universidad de Murcia se compromete a proteger tus datos personales. Esta Política de Privacidad describe la información que recogemos, cómo la utilizamos y cómo la mantenemos segura y protegida.
</p>
<p>
El proyecto Descubre en es gestionado y controlado por la Facultad de Informática de la Universidad de Murcia.
</p>
<p>
Esta política de privacidad tiene como objetivos, entre otros, mostrar la transparencia y la responsabilidad con la que gestionamos sus datos, e indicarle que tiene la posibilidad de elegir con relación a la recogida y uso de sus datos personales. En caso de dudas o quejas referentes a nuestra política de privacidad o a su práctica, rogamos contacte con descubrelainformatica@um.es.
</p>
<p>
2. ¿Quién dirige La web del Proyecto Descubre?
</p>
<p>
La Facultad de Informática de la Universidad de Murcia administra y gestiona el sitio del Proyecto Descubre.
</p>
<p>
3. ¿Necesitan los menores el permiso de los padres o tutores para utilizar la web del Proyecto Descubre?
</p>
<p>
Los niños de 14 años en adelante no necesitan el permiso de sus padres o tutores para poder usar la web del Proyecto Descubre, pero es muy recomendable que durante su uso siempre estén supervisados por un adulto.
</p>
<p>
Para poder crear una cuenta de usuario en el Proyecto Descubre se necesita disponer de una cuenta de correo electrónico a la que se enviará un correo con un enlace de activación. Si el niño no dispone de cuenta propia puede usar la alguno de sus padres o tutores y éstos tendrán la responsabilidad de activar su cuenta como usuario del Proyecto Descubre.
</p>
<p>
4. ¿Es La web del Proyecto Descubre seguro para los menores?
</p>
<p>
Hemos hecho todo lo posible para asegurarnos que el sitio web del Proyecto Descubre sea seguro para que lo usen los niños. Por ejemplo, los niños que no dispongan de correo electrónico propio no podrán obtener acceso al sitio a menos que hayamos recibido la autorización del padre o tutor. Tenga en cuenta que, en consonancia con el enfoque adoptado para otros servicios similares, no podremos verificar que la dirección de correo electrónico proporcionada durante el registro de usuario sea de hecho la de su padre o tutor, ni tampoco la edad del usuario.
</p>
<p>
En el sitio web del Proyecto Descubre no habrá ninguna promoción ni enlaces a otros sitios web u otros servicios de terceros, por ello, los padres se pueden quedar tranquilos que sus hijos utilizan un lugar seguro y protegido.
</p>
<p>
5. ¿Qué datos personales recogemos?
</p>
<p>
Cuando un usuario se inscribe, podrá seleccionar su "alias" y especificar una contraseña. Además podrá indicar, su nombre, sexo, año de nacimiento y el centro donde estudia. Se deberá especificar una dirección de correo electrónico válida donde se recibirá un enlace para confirmar la activación de la cuenta. Además, se podrá especificar si se trata de un estudiante o un profesor. En este último caso, los datos relativos al centro donde trabaja y su nombre aparecerán listados de forma pública en la sección Aprende. De este modo, sus estudiantes podrán localizar su perfile público y unirse a sus grupos para empezar a trabajar con él.
</p>
<p>
6. ¿Dónde se guardarán sus datos personales?
</p>
<p>
Actualmente, guardamos la información personal en un centro de datos situado en Europa. Sin embargo, si esto cambia, tomaremos todas las medidas oportunas para garantizar que la información personal se trate de manera segura y conforme a esta Política de Privacidad, incluso en aquellos países en donde las personas tienen menos derechos legales en relación con su información personal.
</p>
<p>
7. ¿Cómo podemos asegurarle que sus datos personales son guardados en un lugar seguro y protegido?
</p>
<p>
La web del Proyecto Descubre se diseñó utilizando diversos medios técnicos con el fin de asegurar que su información personal esté segura y protegida. Por ejemplo: toda la información personal se guarda en servidores sin acceso directo al exterior. También usamos tecnologías sofisticadas para evitar que ninguna persona ajena pueda obtener acceso a los equipos informáticos que alojan a La web del Proyecto Descubre.
</p>
<p>
8. ¿Compartiremos sus datos personales con alguien más?
</p>
<p>
Nunca cederemos su información personal a terceros.
</p>
<p>
También, en caso de que lo exija la legislación, es posible que debamos compartir su información personal con alguien más.
</p>
<p>
Es posible que compartamos cierta información estadística "no personal" con otros para identificar tendencias de cómo se utiliza La web del Proyecto Descubre. Por ejemplo, cuántas usuarios utilizan La web del Proyecto Descubre o cuántas personas lo visitan por mes. No tiene que preocuparse ya que esta información nunca lo identificará personalmente.
</p>
<p>
9. ¿Puede usted compartir el contenido de la web del Proyecto Descubre con alguien más?
</p>
<p>
Los programas realizados y almacenados por cada usuario pueden ser hechos públicos. A partir de ese momento se mostrarán en la sección Explora y dependiendo de la modalidad en que sean publicados su código fuente será visible o no.
</p>
<p>
10. ¿Durante cuánto tiempo guardaremos sus datos personales?
</p>
<p>
Una vez que esté inscrito en el sitio y haya creado una cuenta para, guardaremos su información personal (alias, nombre, e-mail, año de nacimiento, centro, rol y los programas que cree y decida guardar) durante el tiempo que usted use La web del Proyecto Descubre. Si desea cerrar su cuenta, eliminaremos toda su información personal. También podríamos eliminar su cuenta y su información si no hay actividad durante 1 año. En este caso, primero le enviaremos una notificación por correo electrónico y si no recibimos ninguna respuesta suya en 2 semanas, eliminaremos su cuenta y su información.
</p>
<p>
11. ¿Puede saber qué datos personales suyos obran en nuestro poder y proceder a modificar o cancelar la cuenta?
</p>
<p>
Usted, como titular de sus datos, podrá ejercitar los derecho de acceso, rectificación, supresión, limitación y oposición en los términos recogidos en la legislación de protección de datos ante la Facultad de Informática de la Universidad de Murcia. A tales fines, bastará escribirnos a la siguiente dirección: descubrelainformatica@um.es. Asimismo, puedes contactar con el Delegado de Protección de Datos de la Universidad de Murcia enviando un mensaje desde tu cuenta de correo a esta dirección: privacidad@um.es. Finalmente, tienes derecho a presentar una reclamación ante la Agencia Española de Protección de Datos.
</p>
<p>
12. ¿Cómo puede actualizar sus datos personales?
</p>
<p>
Puede actualizar sus datos personales y otros ajustes en cualquier momento en la web del Proyecto Descubre, en la página de su perfil personal.
</p>
<p>
13. ¿Cuándo nos pondremos en contacto con usted?
</p>
<p>
Como explicamos anteriormente, le enviaremos un correo electrónico para obtener su consentimiento para activar su cuenta de usuario dentro del Proyecto Descubre.
</p>
<p>
Si no hay actividad durante 1 año le enviaremos una notificación por correo electrónico y si no recibimos ninguna respuesta suya en 2 semanas, eliminaremos su cuenta y su información.
</p>
<p>

14. ¿Que son las cookies? ¿Las utilizamos o utilizamos algo parecido?
</p>
<p>
Las cookies son datos que un sitio web transfiere al ordenador de una persona para que el sitio web pueda recordar cierta información y de esta manera pueda facilitar el uso del sitio.
</p>
<p>
Una cookie es como la persona detrás de la caja del restaurante que frecuenta. Esa persona no sabe su nombre, ni donde vive, pero sí lo puede reconocer y recordar su pedido preferido de modo que la próxima vez que usted va al restaurante, sabrá qué es lo que desea pedir y hará que el servicio sea todavía más rápido.
</p>
<p>
Al igual que la mayoría de los sitios web, La web del Proyecto Descubre usa cookies por diversos motivos que ayudarán a mejorar su experiencia online. Por ejemplo, usamos las cookies habituales de PHP (PHPSESSID) para recordar su correo de inicio de sesión.
</p>
<p>
Podrá optar para que su ordenador le avise cada vez que recibe una cookie u optar por no permitir recibir cookies. Para no recibir cookies deberá cambiar la configuración de su explorador. Los exploradores varían entre sí, por ello, probablemente necesite leer el menú Ayuda. Si decide deshabilitar las cookies, es posible que no pueda disfrutar de algunas características de este sitio web. A menos que usted deshabilite las cookies desde la configuración de su explorador, entenderemos que acepta que el sitio web La web del Proyecto Descubre utilice cookies.
</p>
<p>
15. ¿Cambiará esta Política de Privacidad?
</p>
<p>
Es posible que periódicamente debamos actualizar esta Política de Privacidad. En caso de que debamos hacer cambios en esta Política de Privacidad, los publicaremos en el sitio web donde consideremos necesario para que usted esté al tanto de los cambios. Asegúrese de mirar el sitio web con frecuencia, por si hacemos algún cambio en esta Política de Privacidad. En los casos en que el cambio afecte la manera en que gestionamos su información, también le enviaremos un correo electrónico para notificarle dicho cambio.
</p>
<p>
16. ¿Cómo puede contactarnos?
</p>
<p>
Si tiene alguna pregunta o sugerencia sobre esta Política de Privacidad, comuníquese con nosotros en: descubrelainformatica@um.es
</p>
<p>
17. ¿La web del Proyecto Descubre enlaza con otros sitios web?
</p>
<p>
Sólo con la web de la Universidad de Murcia, la de la Facultad de Informática y la de las Olimpiadas Informáticas de la Región de Murcia.
</p>
